    private void selectTableAndWrite()
    {
        Connection c = null;
        Statement stm = null;
        ResultSet query_result=null;
        Integer id = null;
        String imie = null, nazwisko = null;
        try
        {
            c = DataBaseConnection.openConnection();
            stm = c.createStatement();
            query_result = stm.executeQuery("SELECT * FROM mydb.Autor");
            //query_result = QueryExecution.executeQuery(SELECT_ALL);
        }
        catch(SQLException e)
        {
            System.err.println("Nie udało się przeprowadzić zapytania do tabeli Autor");
            return;
        }
        try
        {
            int i = 0;
            while(query_result.next())
            {
                if(i == jTable1.getRowCount())
                    break;
                
                id = query_result.getInt("idAutor");
                jTable1.setValueAt(id, i, 0);
                
                imie = query_result.getString("Imię");
                jTable1.setValueAt(imie, i, 1);
                
                nazwisko = query_result.getString("Nazwisko");
                jTable1.setValueAt(nazwisko, i, 2);
                  
                ++i;
            }
            //teraz usuwamy niepotrzebne kolumny
            if(i<jTable1.getRowCount())
                jTable1.removeRowSelectionInterval(i, jTable1.getRowCount() - 1);
    
            stm.close();
            DataBaseConnection.closeConnection();
        }catch(SQLException e)
        {
            System.err.println("Błąd przy wpisywaniu do tabeli");
        }
        
        
        
    }

